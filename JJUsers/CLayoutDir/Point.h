//*****************************************************************************
#include <math.h>
using namespace std;
//*****************************************************************************
class Point {
public:
  double x, y;
  Point (double x, double y): x(x), y(y) {}
  double length() const {
    return sqrt(x * x + y * y);
  }
  double operator* (const Point &p) const { return x * p.x + y * p.y; }
};

